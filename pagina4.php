            <h1 align="center" style="color:aliceblue">Algunos de los mejores juegos de la Play 1.</h1>
            
            <h3 align="center" style="color:aliceblue">Esta lista NO ES UN RÁNKING, es eso: una lista. Por tanto, la posición 1 no es mejor que la 10. Simplemente, hemos numerado 
                los juegos para que resulte más cómodo de leer, para que sepáis cuántos os quedan. ¡Bueno, vamos allá!.</h3>
<p style="color:aliceblue">
                1. Metal Gear Solid (Konami 1998)
                <p style="color:aliceblue">
                        <img src="metalgear.jpg" align="left" width="200" height="200">Metal Gear Solid es la tercera entrega de la saga creada por Hideo 
                        Kojima tras Metal Gear y Metal Gear 2: Solid Snake, y la primera aventura en la consola de Sony, PlayStation. 
                        Metal Gear Solid iba a ser desarrollado inicialmente para la consola 3DO y su lanzamiento estaba previsto para 
                        1994 bajo el nombre de Metal Gear Solid 3. Debido a la falta de apoyo económico para la creación del juego, 
                        éste fue desarrollado para PlayStation y salió a la venta como Metal Gear Solid el 3 de septiembre de 1998 
                        en Japón y el 21 de octubre en Estados Unidos. El 24 de junio de 1999 el juego también llegó a Europa. 
                <p style="color:aliceblue">
                El argumento gira entorno a Solid Snake, un soldado que deberá infiltrarse en las instalaciones de un grupo de élite 
                conocido como FOXHOUND en la isla de Shadow Moses, en Alaska. Solid Snake tendrá que rescatar rehenes, aniquilar 
                terroristas y evitar un lanzamiento nuclear, todo ello en una trama que se caracteriza por su complejidad y 
                la inmersión del jugador con todo lo que le rodea dentro de la aventura. Metal Gear Solid fue el primer juego de 
                la saga en hacer uso de los gráficos 3D. De hecho, el apellido "Solid" no sólo es por el protagonista de la aventura, 
                también hace referencia a los entornos tan "sólidos" que la tecnología de la época permitió crear para el juego. 
                Además, esta tercera entrega de la saga Metal Gear utilizó el mismo motor del juego para las numerosas cinemáticas 
                de la aventura. Es uno de los juegos más recordados de esta consola, gracias también, en buena parte, a su mítico 
                doblaje en el que Alfonso Vallés ponía voz al protagonista.
                <p style="color:aliceblue">
                    <br><br>
                    2. Soul Reaver (Crystal Dynamics, 1999)
                <p style="color:aliceblue">
                <img src="soulreaver.jpg" align="left" width="200" height="200">Soul Reaver fue el segundo juego de la saga Legacy of Kain, 
                y salió a la venta en PlayStation en 1999. Los hechos de esta segunda entrega ocurren 1500 años después de Blood Omen: 
                Legacy of Kain, y relatan cómo Raziel, hijo de Kain, se convierte en un segador de almas guiado por una fuerza divina, 
                y por si fuera poco, tiene la difícil misión de devolver el equilibrio natural a la ciudad de Nosgoth. El juego fue 
                todo un éxito, y contando con todas las entregas de la saga, las aventuras de Kain y Raziel han vendido más 3,5 millones 
                de copias en todo el mundo. 
                <p style="color:aliceblue">
                        La importancia del argumento, un diseño artístico espectacular y unos jefes finales impresionantes fueron 
                        algunas de las claves del éxito de uno de los mejores juegos de PlayStation. Sin olvidar el sistema de combate 
                        que nos obligaba a utilizar el entorno para alzarnos con la victoria y las enormes dimensiones de Nosgoth, 
                        abierta a la exploración, con muchísimas zonas ocultas y secretos que descubrir.
                <p>
                <br><br>
                <br><br><br>
                <p style="color:aliceblue">
                3. Dino Crisis (Capcom, 1999)
                <p style="color:aliceblue">
                <img src="dinocrisis.jpg" align="left" width="200" height="200">Dino Crisis fue desarrollado bajo la batuta del creador 
                de Resident Evil, Shinji Mikami. Fue desarrollado por Capcom para su lanzamiento en PlayStation en 1999, 
                aunque más adelante se realizaron versiones para Sega Dreamcast y Microsoft Windows en el año 2000. 
                Su mezcla de survival horror con dinosaurios fue un éxito, y el juego consigió entrar en las listas de los más 
                vendidos durante el año de su lanzamiento, vendiendo cerca de 2 millones y medio de copias. La aventura gira 
                entorno a Regina, una agente de inteligencia miembro de las fuerzas especiales SORT. Regina y tres miembros más 
                del equipo son enviados a una isla donde se encuentra una base secreta del gobierno que ha sido atacada por dinosaurios. 
                El equipo deberá escapar de allí y rescatar al Doctor Edward Kirk. El juego tiene varios finales en función de algunas 
                decisiones que tomemos con nuestra protagonsita. 
                <p style="color:aliceblue">
                        Dino Crisis toma prestados numerosos aspectos de la mecánica de juego de Resident Evil, aunque en este caso 
                        Capcom deja a un lado los muertos vivientes y los sustituye por dinosaurios. Durante la trama teníamos que 
                        recorrer numersas instalaciones desoladas mientras resolvíamos acertijos y conseguíamos claves de seguridad 
                        para acceder a nuevas zonas. Todo ello mientras éramos atacados por los dinosaurios. El juego utilzaba gráficos 
                        en 3D, algo que era muy novedoso para una época acostumbrado a los gráficos prerrenderizados.
                <p style="color:aliceblue">
                <br><br><br>
                4. Crash Bandicoot 3: Warped (Naughty Dog, 2002)
                <p style="color:aliceblue">
                <img src="crash.jpg" align="left" width="200" height="200">La tercera entrega de la saga Crash Bandicoot 
                es considerada por muchos como la mejor. Aclamada por la crítica, también fue un éxito en ventas con más de cinco 
                millones y medio de copias vendidas desde su lanzamiento en PlayStation en julio de 2002. Más adelante, Crash Bandicoot 3: Warped 
                también estuvo disponible para descarga en PlayStation Portable y PlayStation 3 a través de PlayStation Network. La aventura 
                narraba como Crash y Coco Bandicoot viajaban a través del tiempo con la complicada misión de recoger todos los cristales que 
                normalmente se encontraban al final de cada nivel. Había 25 cristales en total.
                <p style="color:aliceblue">
                Naughty Dog desarrolló nueva tecnología en el motor gráfico para la creación de Crash Bandicoot 3: Warped, lo que permitió una 
                notable mejora en las texturas con respecto a las anteriores entregas y una mayor profundidad en los escenarios. Además, se 
                idearon nuevos motores para la mecánica de juego con el fin de facilitar la experiencia de cara a los niveles en los que debíamos 
                conducir avionetas o motos de agua. Por ejemplo, se programó a los aviones enemigos para que salieran antes que el de nuestro 
                personaje y así nos diera tiempo a dispararles antes de que éstos se dieran la vuelta y acabaran con nosotros.
                <br><br><br><br><br>
                <p style="color:aliceblue">
                5. FIFA 98 (EA Sports, 1997)
                <p style="color:aliceblue">
                <img src="fifa.jpg" align="left" width="200" height="200">Está considerada como una de las entregas más importantes de FIFA, 
                ya que además de un nuevo motor gráfico, FIFA: Rumbo al Mundial 98 también incluyó por primera vez opciones tácticas, banda 
                sonora oficial, estadios reales y nuevas ligas. Otra de las características de este FIFA 98, es la inclusión del modo de juego 
                "Rumbo al Mundial", que nos permitía pasar la fase de clasificación y jugar el Mundial con cualquiera de las selecciones 
                reconocidas por la FIFA en aquel entonces, un total de 173. 
                <p style="color:aliceblue">
                FIFA 98 salió a la venta en siete plataformas distintas entre 1997 y 1998, fue la tercera edición con gráficos 3D de la quinta 
                generación de consolas. En FIFA 98 podíamos disfrutar de 11 ligas nacionales, y jugar por primera vez en estadios reales, 
                ya que anteriormente los campos de FIFA eran ficticios. FIFA 98 también fue pionero al introducir canciones reales para su 
                banda sonora. Seguro que aún recordáis su tema principal de Blur, Song 2, que sonaba mientras navegábamos por los menús del juego. 
                Además, fue la primera vez que participaron locutores en otros idiomas, en España fueron Manolo Lama y Paco Gonzáles los encargados 
                de comentar y amenizar los partidos. Sólo unos meses más tarde, salió otro juego de la saga, centrado en el campeonato mundial.
                <br><br><br><br><br>
                <p style="color:aliceblue">
                6. Driver 2 (Infogrames, 2000)
                <p style="color:aliceblue">
                <img src="driver2.jpg" align="left" width="200" height="200"> Driver 2 salió a la venta en PlayStation durante el año 2000 de la 
                mano de Infogrames. El juego mejoraba notablemente los gráficos de la primera entrega de la saga, y mantenía la posibilidad de 
                visitar cuatro ciudades diferentes, eso sí, en este caso el nivel de detalle era mayor y el juego aprovechó al máximo la potencia 
                de PlayStation. Además, la variedad de vehículos también es mucho mayor en Driver 2, en el que además de los coches, teníamos 
                camiones, furgonetas y autobuses. Las misiones se mejoraron con respecto a la primera entrega, en su secuela son más variadas 
                y no siempre comenzaban en el piso franco de nuestro personaje como ya ocurriera en Driver, aquí las misiones podían empezar en 
                cualquier punto de la ciudad.
                <p style="color:aliceblue">
                La historia se cuidó al detalle, y sus creadores querían que el jugador se sintiera dentro de la trama. Así pues, la aventura 
                gira en torno a Tranner y su compañero Jones, quienes se encuentran en medio de una guerra de mafiosos. Para resolver la 
                intrincada trama que se nos presenta, Tranner deberá pasar por diferentes ciudades, como La Habana, Las Vegas o Río de Janeiro y 
                poner fin a las organizaciones criminales enfrentadas.
                <br><br><br><br><br>
                <p style="color:aliceblue">
                7. Gran Turismo (Sony Computer Entertainment 1997,1998)
                <p style="color:aliceblue">
                <img src="granturismo.jpg" align="left" width="200" height="200">Gran Turismo es la primera entrega de una de las sagas de 
                conducción más importantes, de hecho el primer Gran Turismo es el juego más vendido de PlayStation con cerca de 11 millones 
                de copias vendidas en todo el mundo. El juego salió a la venta en Japón en 1997, y Estados Unidos y Europa en mayo de 1998. 
                En Gran Turismo podíamos ponernos al volante de 170 coches, y disfrutar de los 11 circuitos del juego. Eso sí, para poder jugar 
                con todos los vehículos debíamos superar diferentes pruebas, ya que no todos estaban disponibles desde el comienzo.
                <p style="color:aliceblue">
                Gran Turismo introduce numerosas novedades, como por ejemplo la utilización del sistema de control Dualshock y una vibración 
                realista nunca antes vista en PlayStation. Gráficamente era bastante espectacular para la época, y si no recordad la superficie 
                de los coches y como brillaba cuando la luz se reflejaba sobre ellos. Además, en Gran Turismo podíamos cambiar la configuración 
                original de los coches, dándole al juego un pequeño plus de personalización. Por otro lado, los coches presumían de un comportamiento 
                muy realista tanto en las curvas, como en los momento de aceleración y frenado. 
                <br><br><br><br><br><br>
                <p style="color:aliceblue">
                8. Resident Evil 2 (Capcom, 1998)
                <p style="color:aliceblue">
                <img src="resident2.jpg" align="left" width="200" height="200"> Aunque también se sopesó una versión para Saturn, Resident Evil 2 
                acabó saliendo en PlayStation en 1998, aunque más adelante se realizarían versiones para otras consolas como Nintendo 64 y Sega 
                Dreamcast. La historia de Resident Evil 2 se sitúa dos meses después de los acontecimientos de la primera entrega de la saga en 
                la Mansión Spencer, cuando los zombies han invadido Raccoon City. Y es ahí donde los destinos de Leon S. Kennedy y Claire Redfield 
                se cruzarían y tendrían que sobrevivir juntos a las aterradoras criaturas que habían tomado la ciudad.
                <p style="color:aliceblue">
                Resident Evil 2 para PlayStation presumía de unas secuencias de video en 3D prerrenderizadas espectaculares. Además, gráficamente 
                superaba con creces lo visto en la primera entrega, destacando el diseño de los escenarios y el modelado de los personajes. 
                Resident Evil 2 también tuvo una versión Dualshock que incluía el soporte para mando analógico con vibración, y que además 
                añadía un modo "novato" al juego.
                <br><br><br><br><br><br><br>
                <p style="color:aliceblue">
                9. Spyro The Dragon (Insomniac Games, 1998)
                <p style="color:aliceblue">
                <img src="spyro.jpg" align="left" width="200" height="200">Spyro es uno de los iconos de PlayStation, y su primera entrega fue muy 
                popular. La aventura estaba protagonizada por el dragón Spyro y una libélula llamada Sparx, aunque el verdadero héroe aquí es el 
                primero. Spyro es el elegido para salvar a su raza y tendrá que que recorrer seis mundos con varios niveles cada uno para ir 
                recolectando gemas, salvando a los demás dragones y preparándose para enfrentarse al malvado Gnasty Gnorc. 
                <p style="color:aliceblue">
                Las animaciones del juego eran increíbles y el juego puede presumir de haber tenido uno de los mejores gráficos de PlayStation. 
                Spyro The Dragon aprovechó al máximo las capacidades de la consola en su momento, que dotaban al juego de unos escenarios bastante 
                grandes sin que con ello se perdiera calidad gráfica. Colorido, original y detallista, no podía faltar en la lista uno de los 
                mejores plataformas 3D de PlayStation.
                <br><br><br><br><br><br><br>
                <p style="color:aliceblue">
                10. Final Fantasy VII (Square, 1997)
                <p style="color:aliceblue">
                <img src="ffxii.jpg" align="left" width="200" height="200">Fue el primer Final Fantasy para PlayStation y el primero en ver la 
                luz en el mercado europeo. La séptima entrega de la saga fue pionera en la introducción de secuencias animadas, así como en la 
                utilización de gráficos 3D. Durante la aventura teníamos que ponernos en la piel de Cloud Strife, y junto al grupo ecoterrorista 
                AVALANCHE, detener a la Corporación Shinra, que utilizaba la vida del planeta como fuente de energía. 
                <p style="color:aliceblue">
                Gráficamente, Final Fantasy VII exprimía al máximo el potencial de PlayStation, la introducción del 3D en la saga supuso un gran 
                paso, aunque en ocasiones esta tridimensionalidad era parcial, por ejemplo en los escenarios con fondos prerrenderizados. 
                Eso sí, durante sus espectaculares combates por turnos el 3D era completo. Además, toda la aventura estaba acompañada de una 
                banda sonora épica, con melodías para el recuerdo.
            </p>

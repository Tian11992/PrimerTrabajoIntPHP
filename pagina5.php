                <h2 align="center" style="color:aliceblue">PlayStation Portable</h2>
                
                    <center>
                        <p style="color:aliceblue">
                        La PlayStation Portable o PSP es una videoconsola portátil de la multinacional de origen japonés, Sony Computer 
                        Entertainment, para videojuegos y multimedia. Se trata de la primera consola portátil a nivel mundial de Sony y la 
                        segunda lanzada en Japón.

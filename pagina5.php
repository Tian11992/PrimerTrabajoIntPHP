                        La consola PSP cambió la tendencia de hacer cada vez aparatos portátiles más pequeños, sacando al mercado una consola 
                        con una pantalla mayor y convirtiéndose en la segunda consola portátil con un D stick (después de la Neo Geo Pocket).
                    </p style="color:aliceblue">

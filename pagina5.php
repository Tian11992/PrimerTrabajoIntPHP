                    <h2 align="center" style="color:aliceblue">PocketStation</h2>
                </p>
                <center>
                    <p style="color:aliceblue">
                        PocketStation es la primera videoconsola portátil de videojuegos creada por Sony que fue enfocada a ser 
                        una tarjeta de memoria interactiva, exclusivamente lanzado en Japón, el 23 de enero de 1999 para la PlayStation, 
                        creado como respuesta al VMU de Sega Dreamcast. Contiene una pantalla LCD, altavoces, un reloj de tiempo real 
                        incorporado e infrarrojos. También se utilizaba como tarjeta de memoria para la videoconsola.
                        <p style="color:aliceblue">
                        Los juegos para esta portátil venían en los mismos CD-ROMs de los juegos de PlayStation, en los que hacía incluir 
                        nuevas características. Por ejemplo, en el videojuego Final Fantasy VIII, se incluía un minijuego llamado Chocobo 
                        World, donde, si se jugaba, se podía conseguir una invocación para el juego de Squaresoft. 
                        En el videojuego Yu-Gi-Oh! Forbidden Memories, facilitaba la obtención de cartas que de otro modo eran imposibles 
                        de obtener o sus requerimientos eran difíciles para el jugador. Uno de los mejores ejemplos era el compatible con 
                        el videojuego Mega Man X6, que incluía gráficos muy parecidos a los de las entregas de Mega Man para NES, aunque 
                        tenía una jugabilidad distinta en cuanto a la manera de juego de los niveles y los enfrentamientos con los jefes. 
                        También se podían conseguir videojuegos multijugador para ella.
                    <p style="color:aliceblue">
                        Aunque no se comercializó fuera del mercado japonés a causa de las bajas ventas, cupo la posibilidad de que 
                        empezase a venderse fuera de su país de origen, ya que, por ejemplo, la versión de Final Fantasy VIII de otras 
                        regiones, como la europea o americana, permitían jugar al videojuego Chocobo World si se poseía una PocketStation. 
                        Aun así, se lanzó una pequeña remesa en las tiendas Argos del Reino Unido.
                </center>

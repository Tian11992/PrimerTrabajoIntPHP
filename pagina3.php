            <h1 align="center" style="color:aliceblue">Descripción y especificaciones técnicas y físicas de la PlayStation 1 (PSX).</h1>
            <p>
                <h2 align="center" style="color:aliceblue">Descripción</h2>
            <p style="color:aliceblue">
            Forma: Tiene la forma de un prisma rectangular de color gris y destaca por su diseño compacto y ligero. 
            Con el lanzamiento de la PSOne, la PlayStation redujo aún más su tamaño y peso, los bordes extremos se curvaron más 
            y el color fue un gris más claro en comparación con el original.

<p style="color:aliceblue">La PlayStation One fue la segunda versión del PlayStation.
<p style="color:aliceblue">
- Parte superior: Allí está el lector de discos, se ubican los botones de encendido y apagado y el de reinicio (reset), por último está 
el botón para abrir la tapa del lector de CD. En la versión PlayStation One, el botón de reinicio es corregido tal como se conoció 
en el original PlayStation y este botón es re ubicado en la parte de encendido.
<p style="color:aliceblue">
- Parte frontal: En la parte frontal de la consola, está un par de ranuras para conectar los mandos y en la parte superior se 
ubican dos ranuras para insertar las Tarjetas de Memoria (Memory Cards en inglés). En la quinta generación de videojuegos, 
aparecieron muchos juegos largos; ya fue un requisito tener las tarjetas de memoria para guardar los avances de los juegos o 
cualquier personalización de un juego.
<p style="color:aliceblue">
- Parte trasera: En ese lugar está un puerto paralelo (que Sony eliminó en las últimas versiones de la consola, presuntamente 
para combatir la piratería de sus juegos porque existen accesorios que, una vez conectados al mismo, permiten eludir las 
protecciones del sistema y ejecutar juegos no originales), un puerto serie, la entrada de alimentación y la salida de A/V. 
Al quitarla, pensaron que consiguieron combatir la piratería, lo que no funcionó y se pudo combatirla por otros sistemas 
(por el implante de chips o conexión de cables internos, eso en caso de que el chip fue compatible con todos los juegos piratas).
<p style="color:aliceblue"> 
- Partes laterales: La consola tiene unas pequeñas rejillas de ventilación que permiten la disipación del calor. 
Se recomendó dejar las partes laterales libres de algún objeto que obstruya la ventilación para así evitar los sobrecalentamientos.
<p style="color:aliceblue">
- Parte inferior: En la parte inferior hay unas rejillas de ventilación; también se ubican los tornillos que sujetan la carcasa y 
finalmente, están las etiquetas de información de la consola.
